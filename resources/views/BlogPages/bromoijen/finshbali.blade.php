    <p class="para one" id="para">
      Bromo Ijen Tour adalah salah satu paket wisata paling populer di Jawa Timur.
      Trip ini dimulai dari <b>Surabaya</b>, mengunjungi sunrise indah di Gunung Bromo,
      menikmati keindahan Lautan Pasir & Bukit Teletubbies, lalu melanjutkan perjalanan ke
      Banyuwangi untuk trekking menuju <b>Kawah Ijen</b>. Puncaknya adalah melihat fenomena
      alam <i>Blue Fire</i> yang hanya ada dua di dunia. Trip ini berakhir di Bali sehingga
      cocok untuk wisatawan yang ingin melanjutkan liburannya ke Pulau Dewata.
    </p>

    <div class="para two">
  <h2>1. Itinerary Bromo Ijen Tour 3 Hari 2 Malam</h2>
  <hr />

  <b>Day 1 (SURABAYA TO HOTEL IN MOUNT BROMO AREA)</b><br />
  Bromo ijen tour akan dimulai dari area Surabaya, pemandu kita dan supir akan menjemput kamu di hotel,
  bandara atau stasiun kereta lalu kita dari Surabaya akan berkendara menuju ke penginapan di kawasan Gunung
  Bromo sekitar 3-4 jam.<br /><br />

  Setibanya anda di hotel, chek-in hotel dan acara bebas. (Kalian bisa mengeksplorasi di sekitar penginapan
  anda atau langsung beristirahat dan nikmati pengalaman menginap yang menyenangkan sebelum anda melakukan tour
  Gunung Bromo.)<br /><br />

  <b>PROGRAM OPSIONAL:</b> Jika Anda bisa berangkat pada pagi hari & cuaca cukup bagus, Anda berkesempatan
  mengunjungi <i>Air Terjun Madakaripura</i> dalam perjalanan dari Surabaya menuju hotel Anda di kawasan Gunung
  Bromo. Silakan cek gambar Air Terjun Madakaripura di Internet.<br /><br />

  <b>Day 2 (BROMO SUNRISE TOUR - HOTEL IN IJEN CRATER AREA)</b><br />
  Tour Sunrise Gunung Bromo akan dimulai sekitar pukul 03.00 dari hotel anda, jeep akan membawa anda ke viewpoint
  paling terkenal di kawasan Gunung Bromo yaitu Penanjakan/Bukit King Kong. Dari bukit tersebut kamu bisa melihat
  matahari terbit terbaik di Jawa Timur dengan latar belakang pegunungan dan gunung berapi.<br /><br />

  <i>(Opsional: bila anda datang ke Gunung Bromo di akhir pekan, tour harus dimulai lebih awal atau mengambil opsi
  menikmati sunrise dari kawah Bromo untuk menghindari keramaian)</i><br /><br />

  Setelah kalian cukup menikmati indahnya pemandangan Gunung Bromo lalu kita kembali ke jeep dan akan melanjutkan
  petualangan hingga mencapai Lautan Pasir di kawasan Gunung Bromo. Kita akan berhenti di area parkir kemudian anda
  akan melintasi Lautan Pasir dengan menunggang kuda atau berjalan kaki hingga mencapai tangga di lereng Gunung
  Bromo yang akan mengantarkan anda mencapai puncak Gunung Bromo.<br /><br />

  Setelah menikmati aktivitas vulkanik di puncak Gunung Bromo anda harus kembali ke jeep anda kemudian jeep akan
  mengantarkan anda kembali ke hotel untuk sarapan pagi, mandi, packing dan memasukkan tas ke dalam mobil.<br /><br />

  Setelah itu kita akan melanjutkan perjalanan menuju ke hotel selanjutnya di kawasan Kawah Ijen sekitar 5-6 jam.
  Pada jalur ini kita akan melewati pemandangan pedesaan, kota kecil, persawahan, perkebunan, dan pantai.
  Untuk makan siang kita bisa antar kalian menuju restoran lokal.<br /><br />

  Sore hari, kami akan sampai di hotel anda di kawasan Kawah Ijen yang hanya berjarak satu jam perjalanan dari
  Kawah Ijen. Anda akan bermalam di sini sebelum menikmati trekking Kawah Ijen keesokan paginya.<br /><br />

  <b>Day 3 (IJEN BLUE FIRE TOUR – DROP BALI)</b><br />
  Trekking menuju Kawah Ijen dimulai tengah malam untuk menyaksikan fenomena <i>Blue Fire</i> yang hanya ada di dua tempat di dunia,
  salah satunya di Kawah Ijen. Setelah itu Anda bisa menikmati keindahan sunrise dari tepi kawah serta aktivitas penambang belerang.
  Setelah turun kembali ke pos, sarapan dan persiapan perjalanan menuju Pelabuhan Ketapang → menyeberang ke Bali (finish tour).
